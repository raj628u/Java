class Test1
{
	public static void main(String args[])
	{
		for(int i=0;i<10;i++)
		{
			System.out.println(i);
		}
		
		System.out.println("While Loop");
		int i=0;
		while(i<5)
		{
			System.out.println(i);
			i++;
		}
		
		
		System.out.println("Do-While Loop");
		int j=0;
		do{
			System.out.println(j);
			j++;
		}
		while(j<8);
	
	
	}
}